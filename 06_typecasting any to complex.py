#By passing one argumrent
#int to complex
a=complex(10)
print(a)

#float to complex
b=complex(10.56)
print(b)

#bool to complex
c=complex(True+True)
print(c)

#str to complex
d=complex('100.56')
print(d)

#By Passing Two Argument
#int to complex
e=complex(10,2)
print(e)

#float to complex
f=complex(10.2,2.4)
